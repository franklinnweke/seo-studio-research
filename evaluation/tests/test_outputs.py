import json
from pathlib import Path
import shutil
from typing import Any

from seo_studio_eval.accounting import build_run_accounting
from seo_studio_eval.blinding import build_blinded_package
from seo_studio_eval.normalization import normalize_run_directories, normalize_run_directory
from seo_studio_eval.records import write_attempt_record
from seo_studio_eval.runner import AttemptSpec, execute_attempt
from seo_studio_eval.schemas import InputEvidence, ModelIdentity, PromptEvidence


class OutputTransport:
    def __init__(self, payload: dict[str, Any]) -> None:
        self.payload = payload

    def generate(self, request: dict[str, Any]) -> dict[str, Any]:
        return {"parsed_payload": self.payload, "done_reason": "stop"}


def make_spec(
    attempt_id: str,
    model_id: str = "qwen35-9b",
    image_id: str = "pilot-001",
) -> AttemptSpec:
    return AttemptSpec(
        experiment_id="pilot-v1",
        protocol_version="2.1-pilot",
        run_id="run-001",
        attempt_id=attempt_id,
        repeat=1,
        randomization_block="block-1",
        git_commit="abc123",
        dirty_worktree=False,
        ollama_version="0.24.0",
        system_snapshot_ref="snapshot.json",
        model=ModelIdentity(
            id=model_id,
            ollama_name="qwen3.5:latest" if model_id == "qwen35-9b" else "qwen2.5vl:3b",
            digest="a" * 64,
            family="Qwen",
            parameters="9B",
            quantization="Q4_K_M",
            license="Apache-2.0",
        ),
        input=InputEvidence(
            image_id=image_id,
            image_sha256="b" * 64,
            dataset_stratum="pilot",
            purpose="informative",
            page_context_sha256="c" * 64,
            brand_profile_sha256="d" * 64,
        ),
        prompt=PromptEvidence(
            prompt_id="vision-facts-v1",
            prompt_sha256="e" * 64,
            schema_sha256="f" * 64,
            system_prompt_sha256="0" * 64,
        ),
        generation_options={"temperature": 0, "seed": 42},
        thinking_mode="disabled",
        sanitized_request={"model": "redacted", "image_sha256": "b" * 64},
    )


def test_normalization_and_blinding_keep_identity_in_private_mapping(tmp_path: Path) -> None:
    run_dir = tmp_path / "runs"
    record = execute_attempt(
        make_spec("attempt-001"),
        OutputTransport({"alt_text": "  A   clinician meets a patient.  ", "warnings": []}),
    )
    raw_path = write_attempt_record(run_dir, record)
    raw_before = raw_path.read_bytes()
    (run_dir / "compatibility-summary.json").write_text('{"status":"compatible"}\n')

    summary, normalized_path = normalize_run_directory(run_dir, tmp_path / "normalized")
    blind_summary, package_path, mapping_path = build_blinded_package(
        normalized_path,
        tmp_path / "released",
        tmp_path / "private",
        seed=42,
    )

    assert summary.status == "normalized"
    assert summary.records_checked == 1
    assert raw_path.read_bytes() == raw_before
    normalized = json.loads(normalized_path.read_text().splitlines()[0])
    assert normalized["output"]["alt_text"] == "A clinician meets a patient."
    assert blind_summary.status == "ready"
    assert "qwen35-9b" not in package_path.read_text()
    assert "qwen3.5:latest" not in package_path.read_text()
    assert "qwen35-9b" in mapping_path.read_text()


def test_blinding_stops_when_output_leaks_model_identity(tmp_path: Path) -> None:
    run_dir = tmp_path / "runs"
    record = execute_attempt(
        make_spec("attempt-leak"),
        OutputTransport({"alt_text": "Generated by qwen3.5:latest"}),
    )
    write_attempt_record(run_dir, record)
    _, normalized_path = normalize_run_directory(run_dir, tmp_path / "normalized")

    summary, package_path, mapping_path = build_blinded_package(
        normalized_path,
        tmp_path / "released",
        tmp_path / "private",
        seed=42,
    )

    assert summary.status == "invalid"
    assert summary.items_written == 0
    assert not package_path.exists()
    assert not mapping_path.exists()


def test_multi_directory_normalization_and_filtered_accounting(tmp_path: Path) -> None:
    first = tmp_path / "run-a"
    second = tmp_path / "run-b"
    image_id = "healthcare-doctor-consultation-001"
    model_ids = [
        "qwen25vl-3b-baseline",
        "qwen25vl-32b-compatibility-fallback",
        "qwen35-9b",
        "gemma3-12b",
        "mistral-small31-24b",
    ]
    for index, model_id in enumerate(model_ids):
        record = execute_attempt(
            make_spec(f"attempt-{index}", model_id=model_id, image_id=image_id),
            OutputTransport({"alt_text": "A clinician meets a patient."}),
        )
        write_attempt_record(first if index < 2 else second, record)

    summary, normalized_path = normalize_run_directories([first, second], tmp_path / "normalized")
    assert summary.status == "normalized"
    assert len(normalized_path.read_text().splitlines()) == 5

    evaluation_root = Path(__file__).resolve().parents[1]
    shutil.copytree(evaluation_root / "configs", tmp_path / "configs")
    shutil.copytree(evaluation_root / "dataset", tmp_path / "dataset")
    accounting = build_run_accounting(
        tmp_path / "configs" / "pilot.toml",
        [first, second],
        tmp_path / "accounting.json",
        model_ids=model_ids,
        image_ids=[image_id],
    )
    assert accounting.status == "complete"
    assert accounting.expected_attempts == 5
    assert accounting.observed_attempts == 5
